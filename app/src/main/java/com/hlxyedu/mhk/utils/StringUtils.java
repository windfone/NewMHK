    public static SpannableStringBuilder getClickableText(String text,
                                                          String clickText, ClickableSpan clickableSpan) {
        SpannableStringBuilder ssb = new SpannableStringBuilder(text);
        int startPos = text.indexOf(clickText);

        ssb.setSpan(clickableSpan, startPos, startPos + clickText.length(), Spannable.SPAN_INCLUSIVE_INCLUSIVE);
        return ssb;
    }
